public class Ball{
  
  //member variables
  public int xPos;
  public int yPos;
  public int xIncrement;
  public int yIncrement;
  
  //constructor
  public Ball(int xPos, int yPos, int xIncrement, int yIncrement) {
    this.xPos = xPos;
    this.yPos = yPos;
    this.xIncrement = xIncrement;
    this.yIncrement = yIncrement;
  }
}
